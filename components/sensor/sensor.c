void ICM_Get_R_Matrix(ICM42688P_Classdef* IMU, float *X, float *Y, float *Z)
{
  float Sen_Data[1000] = {0};
  float Gx,Gy,Gz;
  float Avg = 0.0;
  //噪声矩阵测量
  /*------------------------roll-----------------------------*/
  for(uint16_t i=0;i<1000;i++)
  {
    ICM_42688P_read_GYRO(IMU, &Gx, &Gy, &Gz);
    Sen_Data[0] = Gx;
    *X += Gx;
    vTaskDelay(1);
  }
  Avg = *X / 1000;
  *X = 0.0;
  for(uint16_t i=0;i<1000;i++)
  {
    *X += powf(Sen_Data[i]-Avg,2)/1000;       //求方差
    vTaskDelay(1);
  }
  /*-----------------------pitch-----------------------------*/
  for(uint16_t i=0;i<1000;i++)
  {
    ICM_42688P_read_GYRO(IMU, &Gx,&Gy,&Gz);
    Sen_Data[0] = Gy;
    *Y += Gy;
    vTaskDelay(1);
  }
  Avg = *Y / 1000;
  *Y = 0.0;
  for(uint16_t i=0;i<1000;i++)
  {
    *Y += powf(Sen_Data[i]-Avg,2)/1000;       //求方差
    vTaskDelay(1);
  }
  /*-------------------------yaw-----------------------------*/
  for(uint16_t i=0;i<1000;i++)
  {
    ICM_42688P_read_GYRO(IMU, &Gx,&Gy,&Gz);
    Sen_Data[0] = Gz;
    *Z += Gz;
    vTaskDelay(1);
  }
  Avg = *Z / 1000;
  *Z = 0.0;
  for(uint16_t i=0;i<1000;i++)
  {
    *Z += powf(Sen_Data[i]-Avg,2)/1000;       //求方差
    vTaskDelay(1);
  }
}

